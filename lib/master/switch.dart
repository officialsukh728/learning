
void main() {
  String gfg = "m";
  switch (gfg) {
    case "m":
      {
        print("GeeksforGeeks number 1");
      }
      break;
    case "a":
      {
        print("GeeksforGeeks number 2");
      }
      break;
    case "b":
      {
        print("GeeksforGeeks number 3");
      }
      break;
    default:
      {
        print("This is default case");
      }
      break;
  }
}
